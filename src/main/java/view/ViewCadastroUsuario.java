    private void btnCadastrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCadastrarActionPerformed
        CadastroDao dao = new CadastroDao();
        String nome = txtusuario.getText();
        String login = txtlogin.getText();
        String senha = new String(txtsenha.getPassword());
        String SenhaConfirmacao = new String(txtconsenha.getPassword());
        if (login.isEmpty() || senha.isEmpty() || nome.isEmpty() || SenhaConfirmacao.isEmpty()) {
        labelconfirmacao.setVisible(true);
        labelconfirmacao.setText("Por favor, preencha todos os campos.");
        return;
    }
        if (!senha.equals(SenhaConfirmacao)) {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("As senhas n√£o coincidem!");
            return;
        }
        if (dao.cadastrarUsuario(nome, login, SenhaConfirmacao)) {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("Conta Criada com Sucesso!");
            new view.ViewLogin().setVisible(true);
            this.dispose();
        } else {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("Erro ao criar a conta. Tente novamente.");
        }
    }//GEN-LAST:event_btnCadastrarActionPerformed

