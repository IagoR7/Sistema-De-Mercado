    private void btnalterarsenhaActionPerformed(java.awt.event.ActionEvent evt) {
        AlterarSenhaDao dao = new AlterarSenhaDao();
        String login = txtlogin.getText();
        String novaSenha = new String(txtnovasenha.getPassword());
        String confirmarSenha = new String(txtfonnovasenha.getPassword());

        if (login.isEmpty() || novaSenha.isEmpty() || confirmarSenha.isEmpty()) {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("Por favor, preencha todos os campos.");
            return;
        }
        if (!novaSenha.equals(confirmarSenha)) {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("As senhas n√£o coincidem!");
            return;
        }

        if (dao.alterarSenha(novaSenha, login)) {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("Senha alterada com sucesso!");
        } else {
            labelconfirmacao.setVisible(true);
            labelconfirmacao.setText("Falha ao alterar a senha.");
        }
    }

