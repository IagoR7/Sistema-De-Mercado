    private void btnAcessarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAcessarActionPerformed
        // TODO add your handling code here:
    LoginDao dao = new LoginDao();
String login = txtlogin.getText();
String senha = new String(txtsenha.getPassword());

if (login.isEmpty() || senha.isEmpty()) {
    labelconfirmacao.setVisible(true);
    labelconfirmacao.setText("Por favor, preencha todos os campos.");
    return;
}

Usuario usuarioAutenticado = dao.autenticar(login, senha);

if (usuarioAutenticado != null) {
    // üî• Salva o usu√°rio logado na sess√£o
    SessaoUsuario.setUsuarioLogado(usuarioAutenticado);

    labelconfirmacao.setVisible(true);
    labelconfirmacao.setText("Login efetuado com sucesso!");

    // Abre a tela de cadastro de produto
    new view.ViewGerenciadorProdutos().setVisible(true);
    this.dispose();
} else {
    labelconfirmacao.setVisible(true);
    labelconfirmacao.setText("Login ou senha incorretos.");
}
    }//GEN-LAST:event_btnAcessarActionPerformed

